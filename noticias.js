function aplicarIdiomaNoticias(idioma) {
    const idiomaAtual = traducoesNoticias[idioma] ? idioma : "pt";

    atualizarMenuNavegacao(idiomaAtual);
    atualizarTextoControles(idiomaAtual);
    atualizarConteudoNoticias(idiomaAtual);

    localStorage.setItem("idioma", idiomaAtual);
}

function trocarIdioma(idioma) {
    aplicarIdiomaNoticias(idioma);
}

const darkModeBtn = document.getElementById("darkModeBtn");
const lightModeBtn = document.getElementById("lightModeBtn");
const usBtn = document.getElementById("us");
const ptBtn = document.getElementById("pt");
const esBtn = document.getElementById("es");
const jpBtn = document.getElementById("jp");

const btn = document.getElementById("toggleBtn");
const menu3 = document.getElementById("menu3");

if (btn && menu3) {
    btn.addEventListener("click", () => {
        menu3.classList.toggle("hidden");
    });
}

if (menu3) {
    if (darkModeBtn) {
        darkModeBtn.addEventListener("click", () => {
            document.body.classList.add("dark-mode");
            localStorage.setItem("tema", "dark");
            menu3.classList.add("hidden");
        });
    }

    if (lightModeBtn) {
        lightModeBtn.addEventListener("click", () => {
            document.body.classList.remove("dark-mode");
            localStorage.setItem("tema", "light");
            menu3.classList.add("hidden");
        });
    }

    if (usBtn) {
        usBtn.addEventListener("click", () => {
            trocarIdioma("en");
            menu3.classList.add("hidden");
        });
    }

    if (ptBtn) {
        ptBtn.addEventListener("click", () => {
            trocarIdioma("pt");
            menu3.classList.add("hidden");
        });
    }

    if (esBtn) {
        esBtn.addEventListener("click", () => {
            trocarIdioma("es");
            menu3.classList.add("hidden");
        });
    }

    if (jpBtn) {
        jpBtn.addEventListener("click", () => {
            trocarIdioma("jp");
            menu3.classList.add("hidden");
        });
    }
}

const idiomaSalvo = localStorage.getItem("idioma");
if (document.getElementById("linkNoticias")) {
    trocarIdioma(idiomaSalvo || "pt");
}

const temaSalvo = localStorage.getItem("tema");
if (temaSalvo === "dark") {
    document.body.classList.add("dark-mode");
} else {
    document.body.classList.remove("dark-mode");
}




