function aplicarTexto(id, texto) {
    const elemento = document.getElementById(id);
    if (elemento) {
        elemento.textContent = texto;
    }
}

function atualizarMenuNavegacao(idioma) {
    const traducao = traducoesNoticias[idioma] || traducoesNoticias.pt;

    aplicarTexto("linkNoticias", traducao.noticias);
    aplicarTexto("linkEventos", traducao.eventos);
    aplicarTexto("linkSobre", traducao.sobre);
    aplicarTexto("linkLoja", traducao.loja);
    aplicarTexto("linkComunidade", traducao.comunidade);
    aplicarTexto("linkComoJogar", traducao.comoJogar);
    aplicarTexto("linkEntrar", traducao.entrar);
}

function atualizarTextoControles(idioma) {
    const traducao = traducoesNoticias[idioma] || traducoesNoticias.pt;

    const footer = document.getElementById("footerTexto");
    if (footer) {
        footer.textContent = traducao.footer;
    }

    const lightModeBtn = document.getElementById("lightModeBtn");
    if (lightModeBtn) {
        lightModeBtn.textContent = traducao.lightMode;
    }

    const darkModeBtn = document.getElementById("darkModeBtn");
    if (darkModeBtn) {
        darkModeBtn.textContent = traducao.darkMode;
    }

}

